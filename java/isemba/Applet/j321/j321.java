//　アプレット（２）：レイアウトマネージャー
//
//　　部品の配置や大きさを決定するクラスをレイアウトマネージャという。
//　　レイアウトマネージャーの種類にはつぎのようなものがある。
//
//    　　FlowLayout       部品を横１列に配置する。
//    　　GridLayout       部品を格子状に配置する。
//    　　GridBagLayout    部品を格子状に配置する。多様な配置が可能。
//    　　BorderLayout     部品を上下左右、中央に配置する。
//        CardLayout　　　 同一の部品を重ねて配置する。
//
//　　アプレットのデフォルト・レイアウトマネージャは、FlowLayoutマネージャである。
